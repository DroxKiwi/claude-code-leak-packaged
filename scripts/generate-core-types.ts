/**
 * Regenerates src/entrypoints/sdk/coreTypes.generated.ts from lazy Zod schemas
 * in coreSchemas.ts (replaces missing upstream generate-sdk-types output).
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const ROOT = resolve(import.meta.dirname, '..')
const schemasPath = resolve(ROOT, 'src/entrypoints/sdk/coreSchemas.ts')
const outPath = resolve(ROOT, 'src/entrypoints/sdk/coreTypes.generated.ts')

const src = readFileSync(schemasPath, 'utf8')
const re = /^export const (\w+Schema) = lazySchema/gm
const schemaNames: string[] = []
for (const m of src.matchAll(re)) {
  schemaNames.push(m[1])
}

const header = `// AUTO-GENERATED by scripts/generate-core-types.ts — do not edit by hand
import type { z } from 'zod/v4'
import * as schemas from './coreSchemas.js'

type LazyInfer<F extends () => z.ZodTypeAny> = z.infer<ReturnType<F>>

`

const body = schemaNames
  .map((name) => {
    const typeName = name.replace(/Schema$/, '')
    return `export type ${typeName} = LazyInfer<typeof schemas.${name}>`
  })
  .join('\n')

writeFileSync(outPath, `${header}${body}\n`, 'utf8')
console.log(`Wrote ${schemaNames.length} types to ${outPath}`)
